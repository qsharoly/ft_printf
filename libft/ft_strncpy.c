#include "libft.h"

char	*ft_strncpy(char *dst, char const *src, size_t len)
{
	size_t	i;
	size_t	src_size;

	src_size = ft_strlen(src) + 1;
	i = 0;
	while (i < len && i < src_size)
	{
		dst[i] = src[i];
		i++;
	}
	while (i < len)
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
