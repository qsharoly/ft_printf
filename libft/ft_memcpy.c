#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	if (dest == NULL && src == NULL)
		return (NULL);
	i = 0;
	while (i < n)
	{
		*((char unsigned *)(dest + i)) = *((char unsigned *)(src + i));
		i++;
	}
	return (dest);
}
