int		ft_sqrt(int nb)
{
	int		root;

	if (nb < 0)
		return (0);
	root = 0;
	while (root < nb / 2 + 1)
	{
		if (root * root <= nb && (root + 1) * (root + 1) > nb)
			return (root);
		root++;
	}
	return (0);
}
